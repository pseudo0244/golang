package main

import "fmt"

func main() {

	var x float32 = 170
	var y float32 = 83

	fmt.Printf("Addidtion of %d and %d is %d", x, y, x+y)
	fmt.Printf("Subtraction of %d and %d is %d", x, y, x-y)
	fmt.Printf("Multiplication of %d and %d is %d", x, y, x*y)
	fmt.Printf("Division of %d and %d is %d", x, y, x/y)
	fmt.Printf("Remainder of %d and %d is %d", x, y, x%y)

}
